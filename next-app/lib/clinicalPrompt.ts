export interface ClinicalArticleRequest {
  topic: string;
  category: string;
  hospitalName?: string;
  doctorName?: string;
  imageAnalysis: string;
  imageCount: number;
  articleType: 'case' | 'procedure' | 'comparison' | 'general';
  textLength?: number;
  keywords?: string;
}

