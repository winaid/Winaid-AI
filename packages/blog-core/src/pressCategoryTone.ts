/**
 * 보도자료용 카테고리 톤 가이드 XML 블록 빌더. 미등록 카테고리 → null
 * (호출자가 push skip — fallback 을 강제 주입하지 않음, 기존 단일 톤 동작 100% 호환).
 */
export function buildPressCategoryToneBlock(category: string | undefined | null): string | null {
  if (!category) return null;
  const tone = PRESS_CATEGORY_TONE[category];
  if (!tone) return null;
  return `[${category} 보도자료 톤 가이드]
어조: ${tone.tone}
권장 어휘 (자연스럽게 녹여 사용): ${tone.vocabulary.join(', ')}
금기 표현 (사용 금지): ${tone.avoid.join(', ')}`;
}
