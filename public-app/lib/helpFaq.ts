/**
 * 도메인별 FAQ 모음을 반환 (HelpChatWidget 의 제안 질문 등에서 사용).
 */
export function getFaqsByDomain(domain: HelpDomain): HelpFaqEntry[] {
  return HELP_FAQS.filter(f => f.domain === domain);
}

/**
 * SYSTEM_INSTRUCTION 본문 뒤에 붙일 [FAQ 지식] 블록을 생성.
 * - 도메인 섹션 구분
 * - 각 Q 마다 "Q. …(또는 '…', '…'로도 불려요) → A. …"
 * - aliases 는 앞 6개까지만 포함 (프롬프트 비대화 방지)
 * - 각 A 는 200자 hard cap
 */
export function formatFaqKnowledge(): string {
  const domainOrder: HelpDomain[] = ['blog', 'clinical', 'press', 'refine', 'general'];
  const labels: Record<HelpDomain, string> = {
    blog: '블로그',
    clinical: '임상 글',
    press: '언론보도/보도자료',
    refine: 'AI 보정(Refine)',
    general: '일반',
  };

  const parts: string[] = [];
  for (const d of domainOrder) {
    const entries = getFaqsByDomain(d);
    if (entries.length === 0) continue;
    parts.push(`### ${labels[d]}`);
    for (const e of entries) {
      const aliasStr = e.aliases.slice(0, 5).map(a => `'${a}'`).join(', ');
      const aText = e.a.length > 200 ? e.a.slice(0, 200) : e.a;
      parts.push(`- Q. ${e.q} (또는 ${aliasStr} 로도 불려요)\n  A. ${aText}`);
    }
    parts.push('');
  }
  return parts.join('\n');
}
